package c_Increment_and_Decrement;

public class MyClass {

	public static void main(String[] args) {
		
		int test1 = 5;
		++test1;		// test is now 6
		
		int test2 = 5;
		--test2;	//test2 is now 4
		
		
		int x = 34;
		int y = ++x;	// y is 35
		
		int z = 34;
		int w = z++;	// w is 34;
		
		
		int num1 = 4;
		int num2 = 8;
		num2 += num1;	//num2 = num2 + num1
		// num2 is 12 and num1 is 4
		
		int num3 = 4;
		int num4 = 8;
		num4 -=num3;	//num4 = num4 - num3
		//num4 is 4 and num3 = 4
		

	}

}
